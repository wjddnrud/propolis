package com.woo.infra.modules.codegroup;

public class CodeGroup {

	
	private String seq;
	private String ccg_name;
	private String useNY;
	private String codeCount;
	
	public String getCodeCount() {
		return codeCount;
	}
	public void setCodeCount(String codeCount) {
		this.codeCount = codeCount;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getCcg_name() {
		return ccg_name;
	}
	public void setCcg_name(String ccg_name) {
		this.ccg_name = ccg_name;
	}
	public String getUseNY() {
		return useNY;
	}
	public void setUseNY(String useNY) {
		this.useNY = useNY;
	}
	
	
}
